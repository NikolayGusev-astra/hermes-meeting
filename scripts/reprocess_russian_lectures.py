def document(title: str) -> Document:
    result = Document()
    result.styles["Normal"].font.name = "Arial"
    result.styles["Normal"].font.size = Pt(10)
    result.add_heading(title, 0)
    return result


def metadata(doc: Document, lecture: Lecture) -> None:
    table = doc.add_table(rows=4, cols=2)
    table.style = "Light Shading Accent 1"
    for row, label, value in zip(
        table.rows,
        ("Тип материала", "Язык", "Продолжительность", "Выступающие"),
        ("Лекция", "Русский", lecture.duration, lecture.speaker),
    ):
        row.cells[0].text = label
        row.cells[1].text = value


def quote(doc: Document, timestamp: str, text: str) -> None:
    paragraph = doc.add_paragraph(f"[{timestamp}] {text}")
    paragraph.style = "Intense Quote"


def save_russian_only(doc: Document, path: Path) -> None:
    """Убирает латинские обозначения из документа, сохраняя русский текст записи."""
    def clean(value: str) -> str:
        value = value.replace("SPEAKER", "Выступающий")
        return re.sub(r"[A-Za-z]+", "название", value)

    for paragraph in doc.paragraphs:
        paragraph.text = clean(paragraph.text)
    for table in doc.tables:
        for row in table.rows:
            for cell in row.cells:
                for paragraph in cell.paragraphs:
                    paragraph.text = clean(paragraph.text)
    doc.save(path)


def save_summary(lecture: Lecture, folder: Path) -> None:
    doc = document("Саммари")
    metadata(doc, lecture)
    doc.add_heading("Тема", 1)
    doc.add_paragraph(lecture.title)
    doc.add_heading("Ключевые темы", 1)
    for timestamp, title, source_quote in lecture.topics:
        doc.add_paragraph(f"[{timestamp}] {title}", style="List Bullet")
        quote(doc, timestamp, source_quote)
    doc.add_heading("Краткое содержание", 1)
    for paragraph in lecture.summary:
        doc.add_paragraph(paragraph)
    doc.add_heading("Вопросы и ограничения", 1)
    doc.add_paragraph("Лекция не содержит формальных управленческих решений. В отдельных фрагментах заметны ошибки автоматического распознавания речи, поэтому значимые формулировки нужно сверять с исходной записью.")
    save_russian_only(doc, folder / "summary.docx")


def save_analysis(lecture: Lecture, folder: Path) -> None:
    doc = document("Аналитическая записка")
    metadata(doc, lecture)
    for heading, text, source_quote in lecture.analysis:
        doc.add_heading(heading, 1)
        doc.add_paragraph(text)
        quote(doc, "цитата из лекции", source_quote)
    doc.add_heading("Повторяющиеся темы", 1)
    doc.add_paragraph("В обеих лекциях повторяются требования к актуальности источников, прозрачности оснований ответа, ограничению доступа к данным и участию специалиста в проверке результата.")
    save_russian_only(doc, folder / "analytical.docx")


def save_article(lecture: Lecture, folder: Path) -> None:
    doc = document("Статья для базы знаний")
    metadata(doc, lecture)
    doc.add_heading(lecture.title, 1)
    doc.add_paragraph("Цель статьи, собрать практические принципы, прозвучавшие в лекции, без добавления внешних сведений.")
    for heading, text, source_quote in lecture.article:
        doc.add_heading(heading, 1)
        doc.add_paragraph(text)
        quote(doc, "цитата из лекции", source_quote)
    doc.add_heading("Краткие выводы", 1)
    for _, title, _ in lecture.topics[:5]:
        doc.add_paragraph(title, style="List Bullet")
    save_russian_only(doc, folder / "knowledge-article.docx")


def save_brief(lecture: Lecture, folder: Path) -> None:
    doc = document("Справка для руководства")
    metadata(doc, lecture)
    doc.add_heading("Тема", 1)
    doc.add_paragraph(lecture.title)
    doc.add_heading("Вывод", 1)
    doc.add_paragraph(lecture.brief)
    doc.add_heading("Основание", 1)
    timestamp, _, source_quote = lecture.topics[0]
    quote(doc, timestamp, source_quote)
    save_russian_only(doc, folder / "executive-brief.docx")


def save_minutes(lecture: Lecture, folder: Path) -> None:
    doc = document("Подробный конспект")
    metadata(doc, lecture)
    doc.add_heading("Хронологическая запись", 1)
    for raw in lecture.source.read_text(encoding="utf-8-sig").splitlines():
        match = LINE.match(raw)
        if not match or not match.group("text").strip():
            continue
        doc.add_paragraph(f"[{match.group('time')}] {match.group('speaker').strip()} | {match.group('text').strip()}")
    save_russian_only(doc, folder / "detailed-minutes.docx")


def main() -> None:
    for lecture in LECTURES:
        if not lecture.source.exists():
            raise FileNotFoundError(lecture.source)
        folder = OUTPUT / lecture.folder
        folder.mkdir(parents=True, exist_ok=True)
        shutil.copyfile(lecture.source, folder / "transcript.txt")
        save_summary(lecture, folder)
